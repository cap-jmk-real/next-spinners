/**
 * Copies Loading UI registry components from `vendor/loading-ui/registry/` into
 * `src/loading-ui/` with `@/lib/utils` → `./utils`, then writes:
 * - `src/loading-ui/index.ts` — re-exports components (and `export type` where present)
 * - `examples/loading-ui-overview-app/lib/kit-tiles.tsx` — imports for the overview grid
 *
 * Run: `npm run port:loading-ui` (after `npm run vendor:sync` if upstream changed).
 */
import { mkdirSync, readdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const vendorComponents = join(
  root,
  "vendor",
  "loading-ui",
  "registry",
  "components",
  "loading-ui",
);
const vendorUtils = join(root, "vendor", "loading-ui", "registry", "lib", "utils.ts");
const outDir = join(root, "src", "loading-ui");
const kitTilesPath = join(
  root,
  "examples",
  "loading-ui-overview-app",
  "lib",
  "kit-tiles.tsx",
);

/** @returns {{ values: string[]; types: string[] }} */
function extractExports(source) {
  const types = [];
  const typeBlockRe = /export\s+type\s+(\w+)\s*=/g;
  let tm;
  while ((tm = typeBlockRe.exec(source)) !== null) {
    types.push(tm[1]);
  }

  const braced = source.match(/export\s*\{\s*([^}]+)\s*\}\s*;/);
  if (braced) {
    const values = braced[1]
      .split(",")
      .map((s) => s.trim())
      .filter(Boolean)
      .map((part) => {
        const asM = part.match(/^(\w+)\s+as\s+(\w+)$/);
        return asM ? asM[2] : part;
      });
    return { values, types };
  }

  const fn = source.match(/export\s+function\s+(\w+)\s*\(/);
  if (fn) {
    return { values: [fn[1]], types };
  }

  const cn = source.match(/export\s+const\s+(\w+)\s*=/);
  if (cn) {
    return { values: [cn[1]], types };
  }

  throw new Error(
    "Could not determine export (expected export {…}, export function, or export const)",
  );
}

function main() {
  mkdirSync(outDir, { recursive: true });

  writeFileSync(join(outDir, "utils.ts"), readFileSync(vendorUtils, "utf8"), "utf8");

  const componentFiles = readdirSync(vendorComponents)
    .filter((f) => f.endsWith(".tsx") && f !== "_registry.ts")
    .sort((a, b) => a.localeCompare(b));

  /** @type {{ file: string; base: string; values: string[]; types: string[] }[]} */
  const meta = [];

  for (const file of componentFiles) {
    let body = readFileSync(join(vendorComponents, file), "utf8");
    body = body.replace(/from\s+["']@\/lib\/utils["']/g, 'from "./utils"');
    writeFileSync(join(outDir, file), body, "utf8");
    const { values, types } = extractExports(body);
    meta.push({
      file,
      base: file.replace(/\.tsx$/, ""),
      values,
      types,
    });
  }

  const indexLines = [
    "/** Auto-generated by `npm run port:loading-ui` — upstream: https://loading-ui.com */",
    "",
  ];
  for (const { base, types, values } of meta) {
    for (const t of types) {
      indexLines.push(`export type { ${t} } from "./${base}";`);
    }
    if (values.length) {
      indexLines.push(`export { ${values.join(", ")} } from "./${base}";`);
    }
  }
  indexLines.push("");
  writeFileSync(join(outDir, "index.ts"), indexLines.join("\n"), "utf8");

  mkdirSync(dirname(kitTilesPath), { recursive: true });

  const kitImportLines = meta.map(({ base, values }) => {
    return `import { ${values.join(", ")} } from "../../../src/loading-ui/${base}";`;
  });

  const tileEntries = meta
    .map(({ file, values }) => {
      const label = file.replace(/\.tsx$/, "");
      const cmp = values[0];
      return `  ["${label}", ${cmp}]`;
    })
    .join(",\n");

  const kitBody = [
    "/** Auto-generated by `npm run port:loading-ui` */",
    '"use client";',
    "",
    ...kitImportLines,
    "",
    'import type { ComponentType } from "react";',
    "",
    "/** Heterogeneous loader props — use `any` so the overview grid can hold every registry export. */",
    "export const LOADING_UI_KIT_TILES: readonly [string, ComponentType<any>][] = [",
    tileEntries,
    "] as const;",
    "",
  ].join("\n");

  writeFileSync(kitTilesPath, kitBody, "utf8");

  console.log(
    `Ported ${componentFiles.length} Loading UI components → ${outDir}\nWrote src/loading-ui/index.ts\nWrote examples/loading-ui-overview-app/lib/kit-tiles.tsx`,
  );
}

main();
